    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        //dados Cliente
        Cliente cliente = new Cliente();
        cliente.setNome(jTextFieldNome.getText());
        cliente.setEmail(jTextFieldEmail.getText());
        //Endereco
        cliente.getEndereco().setLogradouro(jTextFieldLogradouro.getText());
        cliente.getEndereco().setCep(jFormattedTextFieldCEP.getText());
        cliente.getEndereco().setBairro(jTextFieldBairro.getText());
        cliente.getEndereco().setCidade(jTextFieldCidade.getText());
        cliente.getEndereco().setUf(jTextFieldUF.getText());

        if (jRadioButtonAtivo.isSelected()) {
            cliente.setAtivo(true);
        } else {
            cliente.setAtivo(false);
        }

        //cliente.setPws(jPasswordFieldSenha.getPassword().toString());
        cliente.setPws(new String(jPasswordFieldSenha.getPassword()));
        String confSenha = new String(jPasswordFieldConfSenha.getPassword());

        String erros = cliente.valida(confSenha);

        if (erros.equals("")) {
            CtrlCliente ctrlCliente = new CtrlCliente();
            ctrlCliente.cadastrar(cliente);
            JOptionPane.showMessageDialog(rootPane, "Cadastrado");
        } else {
            JOptionPane.showMessageDialog(rootPane, erros);
        }


    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jFormattedTextFieldCEPKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFormattedTextFieldCEPKeyReleased

        if (jFormattedTextFieldCEP.getCaretPosition() == 9) {
            Map locais = Locais.buscarCEP(jFormattedTextFieldCEP.getText());
            jTextFieldLogradouro.setText(locais.get("logradouro").toString());
            jTextFieldBairro.setText(locais.get("bairro").toString());
            jTextFieldCidade.setText(locais.get("localidade").toString());
            jTextFieldUF.setText(locais.get("uf").toString());
        }
    }//GEN-LAST:event_jFormattedTextFieldCEPKeyReleased

    private void jButtonBuscarCEPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarCEPActionPerformed
        CtrlEndereco ctrlEndereco = new CtrlEndereco();
        Endereco endereco = ctrlEndereco.pesquisar(jFormattedTextFieldCEP.getText());
        if(endereco == null){
            JOptionPane.showMessageDialog(rootPane, "CEP n√£o localizado.");
            jTextFieldLogradouro.setText("");
            jTextFieldBairro.setText("");
            jTextFieldCidade.setText("");
            jTextFieldUF.setText("");
            
            jTextFieldLogradouro.setEnabled(true);
            jTextFieldBairro.setEnabled(true);
            jTextFieldCidade.setEnabled(true);
            jTextFieldUF.setEnabled(true);
        }else{
            jTextFieldLogradouro.setText(endereco.getLogradouro());
            jTextFieldBairro.setText(endereco.getBairro());
            jTextFieldCidade.setText(endereco.getCidade());
            jTextFieldUF.setText(endereco.getUf());
            
            jTextFieldLogradouro.setEnabled(false);
            jTextFieldBairro.setEnabled(false);
            jTextFieldCidade.setEnabled(false);
            jTextFieldUF.setEnabled(false);
            
        }
    }//GEN-LAST:event_jButtonBuscarCEPActionPerformed

